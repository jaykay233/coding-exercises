#include <bits/stdc++.h>
using namespace std;
class Solution{
public: 
    int minSwaps(vector<int>& grid){
        int n = grid.size();
        vector<int> pos(n,-1);
        for(int i=0;i<n;i++){
            for(int j=n-1;j>=0;j--){
                if(grid[i][j]==1){
                    pos[i]=j;
                    break;
                }
            }
        }
        int ans = 0;
        for(int i=0;i<n;i++){
            int k = -1;
            for(int j=i;j<n;j++){
                if(pos[j]<=i){
                    ans+=j-i;
                    k=j;
                    break;
                }
            }
            if(~k){
                for(int j=k;j>i;j--){
                    swap(pos[j],pos[j-1]);
                }
            }
            else return -1;
        }
        
        return ans;
    }
};